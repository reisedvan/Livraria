    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        String cep = cepTextField.getText();
        String rua = ruaTextField.getText();
        String cidade = cidadeTextField.getText();
        String numero = numeroTextField.getText();
        String bairro = bairroTextField.getText();
        String uf = ufComboBox.getSelectedItem().toString();

        String telefone = telefoneTextField.getText();

        String nome = nomeTextField.getText();
        LibraryApplication.cadastrarEndereco(cep, uf, cidade, bairro, rua, numero);
        LibraryApplication.cadastrarTelefone(telefone);
        LibraryApplication.cadastrarCliente(contador, nome, cep, telefone);
        contador++;

    }//GEN-LAST:event_salvarButtonActionPerformed

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        String x = JOptionPane.showInputDialog(null, "Insira o id");
        Cliente busca = LibraryApplication.buscaCliente(x);

        telefoneTextField.setText(busca.getTelefone().getNumero());
        nomeTextField.setText(busca.getNome());
        bairroTextField.setText(busca.getEndereco().getBairro());
        cidadeTextField.setText(busca.getEndereco().getCidade());
        cepTextField.setText(busca.getEndereco().getCep());
        ruaTextField.setText(busca.getEndereco().getRua());
        numeroTextField.setText(busca.getEndereco().getNumero());
        ufComboBox.setSelectedItem(busca.getEndereco().getUf());

    }//GEN-LAST:event_buscarButtonActionPerformed

